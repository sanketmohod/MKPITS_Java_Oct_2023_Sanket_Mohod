// swapping number by using third variable
let num1 = 20;
let num2 = 30;
let temp;
// numbers before swapping
console.log("Before Swapping");
console.log("value of num1 : " + num1);
console.log("value of num2 : " + num2);
// numbers after swapping
temp = num1;
num1 = num2;
num2 = temp;
console.log("\nAfter Swapping");
console.log("value of num1 : " + num1);
console.log("value of num2 : " + num2);
