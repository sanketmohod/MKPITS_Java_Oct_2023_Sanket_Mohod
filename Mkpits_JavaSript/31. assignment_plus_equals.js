// assignment operator(+=)
// use to add left operand value with right operand and assign value to left operand.
let a = 40;
a +=  12; // here value of a=40 is adding with 12 and  then assigned back to 'a'. so now the value of 'a' will be 52.
console.log(a);
let b =  15;
b += a; //  same as b = b + a;
console.log(b);