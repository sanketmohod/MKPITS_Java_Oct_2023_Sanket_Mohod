// Demostrate the use of switch case statement in javascript
// The program will ask user to enter a number and then it will display what is that number.
let num = prompt("Enter a number");
switch (num) {
  case "1":
    alert("Monday");
    break;
  case "2":
    alert("Tuesday");
    break;
  case "3":
    alert("Wensday");
    break;
  case "4":
    alert("thursday");
    break;
  case "5":
    alert("Friday");
    break;
  default:
    alert("Invalid");
    break;
}
// code is run on browser
