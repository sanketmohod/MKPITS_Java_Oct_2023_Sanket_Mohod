// bitwise and(&) operator
// The operator returns true only if both the operands are true. Otherwise, it returns false. 
// 0 & 1, 1 & 0 = 0      0 & 0 = 0, 1 & 1 = 1
let a = 7; // 111
let b = 5; // 101
console.log(a & b); // 101 -> 5

let c = 9; // 100
let d = 3; // 011
console.log(c & d); // 001 -> 1
