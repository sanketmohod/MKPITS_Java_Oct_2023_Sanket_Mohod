function operations(num1, num2) {
  let sum = num1 + num2;
  let sub = num1 - num2;
  let mul = num1 * num2;
  let div = num1 / num2;
  let mod = num1 % num2;
  console.log("sum is : " + sum);
  console.log("sub is : " + sub);
  console.log("mul is : " + mul);
  console.log("div is : " + div);
  console.log("mod is : " + mod);
}

// calling parmeteries function

operations(25, 10);
